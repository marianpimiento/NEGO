    void CargarTablaClientes()
    {
        String[] titulos = {"ID", "Cedula", "P. Nombre", "Otros Nombre","P. Apellido", "S. Apellido",  "Direccion", "Telefono", "Celular", "E-mail"};
        String[] registro = new String[10];
        String sSQL = "";
        
        modelo = new DefaultTableModel(null, titulos);
        
        //ConexionMySQL mysql = new ConexionMySQL();
        //Connection cn = mysql.Conectar();
        
        sSQL = "SELECT * FROM clientes ";
        //       + "WHERE CONCAT(apellido1, ' ', apellido2, ' ',nombre1, ' ',nombre2) LIKE '%"+valor+"%'";
        
        try 
        {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sSQL);
            
            while(rs.next())
            {
                registro[0] = rs.getString("idcliente");
                registro[1] = rs.getString("cedula");
                registro[2] = rs.getString("nombre1");
                registro[3] = rs.getString("nombre2");
                registro[4] = rs.getString("apellido1");
                registro[5] = rs.getString("apellido2");
                registro[6] = rs.getString("direccion");
                registro[7] = rs.getString("telefono");
                registro[8] = rs.getString("celular");
                registro[9] = rs.getString("email");
                
                modelo.addRow(registro);
            }
            
            tblConsultaCliente.setModel(modelo);
            
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }        
    }
    
    
    


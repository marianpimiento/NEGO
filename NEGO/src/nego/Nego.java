    private void btnGuardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarClienteActionPerformed
        
        String ced, nom1, nom2, ap1, ap2, dir, tel, cel, email;
        String sSQL = "";
        String mensaje = "";
        
        ced = txtCedulaCliente.getText();
        nom1 = txtNombre1Cliente.getText();
        nom2 = txtNombre2Cliente.getText();
        ap1 = txtApellido1Cliente.getText();
        ap2 = txtApellido2Cliente.getText();
        dir = txtDireccionCliente.getText();
        tel = txtTelefonoCliente.getText();
        cel = txtCelularCliente.getText();
        email = txtEmailCliente.getText();
        
        if(accionClientes.equals("Insertar"))
        {            
            sSQL = "INSERT INTO clientes(cedula, nombre1, nombre2, apellido1, apellido2, direccion, telefono, celular, email)"
                    + "VALUES(?, ?, ?, ?, ?, ?, ?, ?, ?)";
            
            mensaje = "Los datos se han Insertado";
        }
        else if(accionClientes.equals("Modificar"))
        {
            /*sSQL = "UPDATE datos_personales "
                    + "SET apellido1 = ?,"
                    + "apellido2 = ?,"
                    + "nombre1 = ?,"
                    + "nombre2 = ?,"
                    + "fecha_nac = ?,"
                    + "genero = ? "
                    + "WHERE id_emp = "+id_actualizar;
                               
            mensaje = "Los datos se han Modificado"; */
        }
        
        try
        {
            PreparedStatement pst = cn.prepareStatement(sSQL);
            pst.setString(1, ced);
            pst.setString(2, nom1);
            pst.setString(3, nom2);
            pst.setString(4, ap1);
            pst.setString(5, ap2);
            pst.setString(6, dir);
            pst.setString(7, tel);
            pst.setString(8, cel);
            pst.setString(9, email);
            
            inhabilitarCliente();  //Marian
            
            int n = pst.executeUpdate();
            
            if (n > 0)
            {
                JOptionPane.showMessageDialog(null, mensaje);
                CargarTablaClientes();
            }
            
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btnGuardarClienteActionPerformed

